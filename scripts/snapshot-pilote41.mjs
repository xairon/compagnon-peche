// Snapshot ALL of Loir-et-Cher (41) fishing parcours & reserves from Pilote41
// (the department's public ArcGIS service) into a static, offline-bundled file:
//   src/data/parcours41-snapshot.ts
// Produces the SAME normalized FeatureCollection that lib/parcours41.ts built at
// runtime, so the map renders it identically — but with no network needed.
// Run: node scripts/snapshot-pilote41.mjs
import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const SVC = "https://websig.pilote41.fr/ags04/rest/services/PECHE/Peches_MAJ/MapServer";

const CATEG = { "1": "1ʳᵉ catégorie", "2": "2ᵉ catégorie" };
const TYPE = { "1": "Cours d'eau public", "2": "Cours d'eau privé", "3": "Plan d'eau" };
const RIVE = { "1": "rive droite", "2": "rive gauche", "3": "les deux rives" };
const str = (v) => String(v ?? "").trim();

async function query(layer, fields) {
  const url =
    `${SVC}/${layer}/query?where=1%3D1&outFields=${fields}` +
    `&outSR=4326&f=geojson&resultRecordCount=2000`;
  const r = await fetch(url);
  if (!r.ok) throw new Error(`Pilote41 layer ${layer}: ${r.status}`);
  const j = await r.json();
  return j.features || [];
}

const [parc, rperm, rtemp] = await Promise.all([
  query(1, "nom,type,categ_pisc,nuit,rive,acces,url_resa"),
  query(36, "limit_amon,limit_aval,rive"),
  query(37, "limit_amon,limit_aval,rive"),
]);

const features = [];
for (const f of parc) {
  const p = f.properties || {};
  features.push({
    type: "Feature",
    geometry: f.geometry,
    properties: {
      kind: "parcours",
      nom: str(p.nom) || "Parcours de pêche",
      type: TYPE[str(p.type)] || "",
      categ: CATEG[str(p.categ_pisc)] || "",
      nuit: str(p.nuit) === "1" ? "oui" : "",
      rive: RIVE[str(p.rive)] || "",
      acces: str(p.acces),
      url: str(p.url_resa),
    },
  });
}
for (const [list, kindLabel] of [
  [rperm, "permanente"],
  [rtemp, "temporaire"],
]) {
  for (const f of list) {
    const p = f.properties || {};
    features.push({
      type: "Feature",
      geometry: f.geometry,
      properties: {
        kind: "reserve",
        nom: `Réserve de pêche (${kindLabel})`,
        amont: str(p.limit_amon),
        aval: str(p.limit_aval),
      },
    });
  }
}

const fc = { type: "FeatureCollection", features };
const ts =
  `// GENERATED by scripts/snapshot-pilote41.mjs — offline snapshot of the\n` +
  `// Loir-et-Cher (41) parcours & réserves (source: Pilote41 / Fédération 41).\n` +
  `// ${parc.length} parcours + ${rperm.length + rtemp.length} réserves. Regenerate to refresh.\n` +
  `// Do not hand-edit.\n\n` +
  `export const PARCOURS41_SNAPSHOT: GeoJSON.FeatureCollection =\n${JSON.stringify(fc)};\n`;

const out = join(root, "src/data/parcours41-snapshot.ts");
await writeFile(out, ts, "utf8");
const kb = Math.round(Buffer.byteLength(ts) / 1024);
console.log(`Wrote ${features.length} features to ${out} (${kb} KB).`);
